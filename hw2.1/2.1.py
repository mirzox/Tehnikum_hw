def check_operand(char):
    if char in ['+','-','*','/','0']:
        return True
    else:
        False

def operations(a,b,char):
    if char == '+':
        return a+b
    elif char == '-':
        return a-b if a>b else b-a
    elif char == '*':
        return a*b
    elif char == '/':
        try:
            return a/b
        except ZeroDivisionError:            
            return 'ZeroDivisionError'
    elif char == '0':
        return 'END'
    

